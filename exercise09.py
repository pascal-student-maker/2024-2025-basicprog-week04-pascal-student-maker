def max_en_min_uit_list(list_numbers: list[int]) -> int:
   
    max(list_numbers) 
    return max(list_numbers)

    
    
print("\nA list can also be used as an parameter.")  
list_numbers = [100,200,300,400,500,600.700,800] 
print(f"Orginal list: {list_numbers}")
max = max_en_min_uit_list(list_numbers)
print(f"check after function change list: {max}")


 
